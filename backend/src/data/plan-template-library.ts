export function getPlanTemplateSummaries() {
  return PLAN_TEMPLATE_LIBRARY.map(
    ({
      id,
      name,
      summary,
      goal_category,
      experience_level,
      sessions_per_week,
      session_length_minutes,
      phase_1_weeks,
      training_style,
      intensity_label,
      mesocycle_type,
      session_blueprints,
    }) => ({
      id,
      name,
      summary,
      goal_category,
      experience_level,
      sessions_per_week,
      session_length_minutes,
      phase_1_weeks,
      training_style,
      intensity_label,
      mesocycle_type,
      has_session_blueprints: Boolean(
        session_blueprints && session_blueprints.length > 0
      ),
    })
  );
}

export function getPlanTemplateById(
  id: string
): PlanTemplateDefinition | undefined {
  return PLAN_TEMPLATE_LIBRARY.find((t) => t.id === id);
}
